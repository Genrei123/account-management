    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
       int new_stock = 0;
       
       Database updateInventory = new Database();
       
       int inventory_id = Integer.parseInt(id.toString());
       String item_name1 = item_name.toString();
       LocalDate date_now = LocalDate.now();
       new_stock = Integer.parseInt(stockText.getText()) - Integer.parseInt(quantityText.getText());
       Double new_cost = Double.parseDouble(cost.toString());
       String account_name = clientName.getText();
       String account_no = clientNo.getText();
       
       
       
        try {
            //updateInventory(int inventoryId, String item_name, LocalDate date, int stock, Double cost, String account_no)//
            updateInventory.updateInventory(inventory_id, item_name1, date_now, new_stock, new_cost, account_no);
        } catch (SQLException ex) {
            Logger.getLogger(AddTransaction.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
       
       Database addClient = new Database();
        try {
            addClient.addClient(account_name, account_no);
        } catch (SQLException ex) {
            Logger.getLogger(AddTransaction.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }//GEN-LAST:event_jButton1ActionPerformed

