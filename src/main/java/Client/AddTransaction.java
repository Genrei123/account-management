    private void performActionOnRow(int row) {
        id = inventoryTable.getValueAt(row, 0);
        item_name = inventoryTable.getValueAt(row, 1);
        date = inventoryTable.getValueAt(row, 2);
        purchased = inventoryTable.getValueAt(row, 3);
        sold = inventoryTable.getValueAt(row, 4);
        cost = inventoryTable.getValueAt(row, 5);
        stock = inventoryTable.getValueAt(row, 6);
        //System.out.println("Row: " + row + " clicked with data: " + data);
        
        costText.setText(cost.toString());
        stockText.setText(stock.toString());
        itemText.setText(item_name.toString());
    }
    
    private void inventoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inventoryTableMouseClicked
        // TODO add your handling code here:
        int row = inventoryTable.rowAtPoint(evt.getPoint());
        
        if (row >= 0) {
            performActionOnRow(row);
        }
    }//GEN-LAST:event_inventoryTableMouseClicked


